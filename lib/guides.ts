export function getSectionGuide(id: string): SectionGuide | undefined {
  return SECTION_GUIDES.find((s) => s.id === id);
}

export function trainerGuides(): SectionGuide[] {
  return SECTION_GUIDES.filter((s) =>
    ["numbers", "words", "order", "pairs", "images", "longterm"].includes(s.id),
  );
}
