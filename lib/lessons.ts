const SEEN_PREFIX = "pamyat10-lesson-seen-";

export function isLessonSeen(id: TrainerId): boolean {
  if (typeof window === "undefined") return true;
  try {
    return localStorage.getItem(`${SEEN_PREFIX}${id}`) === "1";
  } catch {
    return true;
  }
}

export function markLessonSeen(id: TrainerId): void {
  try {
    localStorage.setItem(`${SEEN_PREFIX}${id}`, "1");
  } catch {
    /* ignore */
  }
}
